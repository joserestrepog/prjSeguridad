    private void btnAgregarEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarEActionPerformed
        // TODO add your handling code here:
        pkgClass.clsDAOEquipo obj;
        obj = new pkgClass.clsDAOEquipo();
        obj.setCedula(txtCedulaE.getText());
        obj.setNombrej(txtNombreE.getText());
        obj.setEdad(txtEdadE.getText());
        obj.setPosicion(txtPosicionE.getText());
        obj.setEquipo(txtEquipoE.getText());

        if(obj.getCedula().equals("") || obj.getNombrej().equals("") || obj.getEdad().equals("") || obj.getPosicion().equals("")  || obj.getEquipo().equals("")){
            JOptionPane.showMessageDialog(rootPane, "No hay información en todos los campos.");
        }else{
            if (obj.insertar() == false){
                JOptionPane.showMessageDialog(rootPane, "Se insertó.");                
            }else{
                JOptionPane.showMessageDialog(rootPane, "No se insertó.");
            }
        }
        txtCedulaE.setText("");
        txtNombreE.setText("");
        txtEdadE.setText("");
        txtPosicionE.setText("");
        txtEquipoE.setText("");
    }//GEN-LAST:event_btnAgregarEActionPerformed

