    private void btnEliminarEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarEActionPerformed
        // TODO add your handling code here:
        pkgClass.clsDAOEquipo obj;
        obj = new pkgClass.clsDAOEquipo();
        obj.setCedula(txtCedulaE.getText());
        
        if(obj.getCedula().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Ingrese la cédula del jugador que desea eliminar.");
        }else{
            obj.borrar();
            if(obj.borrar() == false){
                JOptionPane.showMessageDialog(rootPane, "Se eliminó.");
            }else{
                JOptionPane.showMessageDialog(rootPane, "No se eliminó.");
            }
        }
        txtCedulaE.setText("");
        txtNombreE.setText("");
        txtEdadE.setText("");
        txtPosicionE.setText("");
        txtEquipoE.setText("");
    }//GEN-LAST:event_btnEliminarEActionPerformed

    private void btnConsultarEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarEActionPerformed
        // TODO add your handling code here:
        pkgClass.clsDAOEquipo obj;
        obj = new pkgClass.clsDAOEquipo();
        obj.setCedula(txtCedulaE.getText());        
        String resultado1 = obj.consultar1();        
        String resultado2 = obj.consultar2();        
        String resultado3 = obj.consultar3();        
        String resultado4 = obj.consultar4();
        if(obj.getCedula().equals("")){
           JOptionPane.showMessageDialog(rootPane, "Ingrese la cédula del jugador que desea consultar."); 
        }else{
            if(resultado1.equals("no")){
                JOptionPane.showMessageDialog(rootPane, "No está.");
                txtCedulaE.setText("");
                txtNombreE.setText("");
                txtEdadE.setText("");
                txtPosicionE.setText("");
                txtEquipoE.setText("");
            }else{
                txtNombreE.setText(resultado1);
                txtEdadE.setText(resultado2);
                txtPosicionE.setText(resultado3);
                txtEquipoE.setText(resultado4);
            }            
        }  
    }//GEN-LAST:event_btnConsultarEActionPerformed

