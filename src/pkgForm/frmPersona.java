    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:       
        pkgClass.clsDAOPersona obj;
        obj = new pkgClass.clsDAOPersona();
        obj.setCed(txtCedula.getText());
        obj.setNombre(txtNombre.getText());
        
        if(obj.getCed().equals("") || obj.getNombre().equals("")){
           JOptionPane.showMessageDialog(rootPane, "No hay información en todos los campos."); 
        }else{            
            if (obj.insertar() == false){
                JOptionPane.showMessageDialog(rootPane, "Se insertó.");
                listar();
            }else{
                JOptionPane.showMessageDialog(rootPane, "No se insertó.");
            }
        }
        txtCedula.setText("");
        txtNombre.setText("");
    }//GEN-LAST:event_btnAgregarActionPerformed

