    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        pkgClass.clsDAOPersona obj;
        obj = new pkgClass.clsDAOPersona();
        
        
        int fila=tblDatos.getSelectedRow();
        if (fila != -1){
            String codigo = tblDatos.getValueAt(fila, 0).toString();
            obj.setCed(codigo);
            obj.borrar();
            listar();
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "Para eliminar debe seleccionar una fila.");
        }
        txtCedula.setText("");
        txtNombre.setText("");
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        // TODO add your handling code here:
        pkgClass.clsDAOPersona obj;
        obj = new pkgClass.clsDAOPersona();
        obj.setCed(txtCedula.getText());        
        String resultado=obj.consultar();
        if(obj.getCed().equals("")){
           JOptionPane.showMessageDialog(rootPane, "Ingrese la cédula del jugador que desea consultar."); 
        }else{
            if(resultado.equals("no")){
                JOptionPane.showMessageDialog(rootPane, "No está.");
                txtCedula.setText("");
                txtNombre.setText("");
            }else{
                txtNombre.setText(resultado);
            }            
        }        
    }//GEN-LAST:event_btnConsultarActionPerformed

